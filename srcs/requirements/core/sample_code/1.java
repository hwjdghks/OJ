import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        // 두 정수를 입력받습니다.
        int a = scanner.nextInt();
        int b = scanner.nextInt();

        // 두 정수의 합을 출력합니다.
        System.out.println(a + b);
    }
}
